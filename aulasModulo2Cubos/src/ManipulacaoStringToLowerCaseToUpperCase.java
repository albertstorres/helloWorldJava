public class ManipulacaoStringToLowerCaseToUpperCase {
    public static void main (String[] args) {
        String variavel = "Albert Sevy";
        String variavel_upper = variavel.toUpperCase();
        String variavel_lower = variavel.toLowerCase();

        System.out.println(variavel);
        System.out.println(variavel_upper);
        System.out.println(variavel_lower);
    }
}
