public class Main {
    public static void main (String[] args) {
        int num = 2;

        switch (num) {
            case 1 :
                System.out.println("O número é um.");
                break;
            case 2 :
                System.out.println("O número é dois.");
                break;
            case 3 :
                System.out.println("O número é três.");
                break;
            default :
                System.out.println("O número não está previsto.");
                break;
        }
    }
}